package model;

public class RigaOrdinazione {
	
	private int nRiga;
	private int quantita;
	
	
	public int getnRiga() {
		return nRiga;
	}
	public void setnRiga(int nRiga) {
		this.nRiga = nRiga;
	}
	public int getQuantita() {
		return quantita;
	}
	public void setQuantita(int quantita) {
		this.quantita = quantita;
	}
	
}